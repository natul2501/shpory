function f_doCommand(index){
    let contentBlock;
    if(commArr[index].messageType === "message-text"){
        document.getElementById("message-embed").style.display = "none";
        contentBlock = document.getElementById("message-text");
        contentBlock.style.display = "block";
        let messageBlock = document.getElementById('message');
        messageBlock.style.backgroundColor = 'rgba(0,0,0,0.0)';
        messageBlock.style.borderLeft = 'none';
    }
    if(commArr[index].messageType === "message-text-mentioned"){
        document.getElementById("message-embed").style.display = "none";
        contentBlock = document.getElementById("message-text");
        contentBlock.style.display = "block";
        let messageBlock = document.getElementById('message');
        messageBlock.style.backgroundColor = '#3B3734';
        messageBlock.style.borderLeft = 'solid 3px #CE9C5C';
    }
    if(commArr[index].messageType === "message-embed"){
        document.getElementById("message-text").style.display = "none";
        contentBlock = document.getElementById("message-embed");
        contentBlock.style.display = "block";
        let messageBlock = document.getElementById('message');
        messageBlock.style.backgroundColor = 'rgba(0,0,0,0.0)';
        messageBlock.style.borderLeft = 'none';
    }
    
    let msgInput = `<div id="commName">${commArr[index].msgInput}</div>`;
    if(commArr[index].params){
        for(let param of commArr[index].params){
            msgInput += `<div id="commParams"><div id="paramName">${param.name}</div><div id="paramValue">${param.value}</div></div>&nbsp;&nbsp;&nbsp;`;
        }
    }
    document.getElementById('msgInput').innerHTML = msgInput;
    contentBlock.innerHTML = commArr[index].msgOutput;
    if(commArr[index].buttons){
        let buttField = ''
        document.getElementById('message-buttons').style.display = 'block';
        for(const button of commArr[index].buttons){
            buttField += `<div class="msgButt">${button}</div>`
        }
        document.getElementById('message-buttons').innerHTML = buttField;
    } else {
        document.getElementById('message-buttons').style.display = 'none';
    }
    if(commArr[index].details){
        document.getElementById('explains').innerHTML = commArr[index].details;
    } else {
        document.getElementById('explains').innerHTML = '';
    }
}

